/**
 * @param {number[]} nums
 * @return {number}
 */
var specialArray = function (nums) {
    nums.sort((a, b) => a - b)
    let max = nums[nums.length - 1];

    while (max) {
        let count = 0;
        nums.forEach((val, index) => {
            if (val >= max) count++
        })
        if (count == max) return max
        max--
    }
    return -1

};


